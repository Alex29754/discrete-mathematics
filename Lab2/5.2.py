def count_paths(m, n):
    # Создаем два массива: dp_h[i][j] - последний шаг горизонтальный, dp_v[i][j] - вертикальный
    dp_h = [[0] * (n + 1) for _ in range(m + 1)]
    dp_v = [[0] * (n + 1) for _ in range(m + 1)]

    # Базовый случай: начальная точка (0,0)
    dp_h[0][0] = 1  # Можно считать, что начало пути - "горизонтальный" шаг

    for i in range(m + 1):
        for j in range(n + 1):
            # Горизонтальный шаг (из (i-1, j))
            if i > 0:
                dp_h[i][j] += dp_h[i - 1][j] + dp_v[i - 1][j]
            # Вертикальный шаг (из (i, j-1)), только если предыдущий шаг не был вертикальным
            if j > 0:
                dp_v[i][j] += dp_h[i][j - 1]

    # Общее количество путей - сумма последних шагов
    return dp_h[m][n] + dp_v[m][n]


m = 18
n = 15
print(count_paths(m, n))